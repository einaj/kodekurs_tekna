# C = (5/9)(F - 32)

## 3a)
farenheit = float(input('Gi meg en temperatur i grader Farenheit: '))
celsius = (5/9)*(farenheit - 32)
print(f'{farenheit:.2f}F er det samme som {celsius:.2f}C')

